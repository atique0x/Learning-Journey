// Constants
console.log(Math.PI); // 3.141592653589793
console.log(Math.SQRT2); // 1.4142135623730951

// Rounding
console.log(Math.round(2.2)); // 2 - Nearest integer
console.log(Math.round(2.8)); // 3

console.log(Math.ceil(2.2)); // 3 - Round up
console.log(Math.ceil(2.8)); // 3

console.log(Math.floor(2.2)); // 2 - Round down
console.log(Math.floor(2.8)); // 2

console.log(Math.trunc(2.2)); // 2 - Remove decimal part
console.log(Math.trunc(2.6)); // 2

// Min/Max
console.log(Math.min(1, 5, 10, -2)); // -2
console.log(Math.max(1, 5, 10, -2)); // 10

// Powers and roots
console.log(Math.pow(2, 3)); // 8 (2^3)
console.log(Math.sqrt(16)); // 4
console.log(Math.cbrt(27)); // 3

// Absolute value
console.log(Math.abs(-25)); // 25

// Random
console.log(Math.random()); // Random between 0 and 1
console.log(Math.random() * 10); // Random between 0 and 9.999...
console.log(Math.floor(Math.random() * 101)); // Random between 0 and 100
